#include <iostream>

// Use the standard namespace
using namespace std;

int main() {
    // Declare variables
    int a = 5;
    int b = 10;
    int sum = 0;

    // Calculate sum
    sum = a + b;

    // Output the result
    cout << "The sum of " << a << " and " << b << " is " << sum << endl;

    return 0;
}